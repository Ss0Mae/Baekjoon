def solution(A, B):
    answer = 0
    
    # 1. 두 팀의 숫자를 모두 오름차순(작은 것부터)으로 정렬합니다.
    A.sort(key = lambda x : x)
    B.sort()
    
    # A팀의 카드를 가리킬 인덱스 포인터
    a_idx = 0
    
    # 2. B팀의 카드를 작은 것부터 하나씩 꺼내어 A팀과 비교합니다.
    for b in B:
        # B팀의 현재 카드가 A팀의 현재 카드보다 크다면? (승리!)
        if b > A[a_idx]:
            answer += 1   # 승점 1점 추가
            a_idx += 1    # A팀도 다음(더 큰) 카드로 넘어감
            
        # 만약 B팀의 카드가 A팀 카드보다 작거나 같다면? (패배 또는 무승부)
        # 이 카드는 A팀의 카드를 이길 수 없으므로, 그냥 "버리는 카드"로 씁니다.
        # (a_idx를 증가시키지 않으므로, 다음 번 B팀의 더 큰 카드가 같은 A팀 카드를 상대하게 됩니다.)
            
    return answer